# ---------------------------------------------
# ------------- Comparison Operators -------------
# ---------------------------------------------
# [1] ==  Equal
# [2] !=  Not Equal
# [3] >   Greater Than
# [4] <   Less Than
# [5] >=  Greeter Than or Equal
# [6] <=  Less Than or Equal
# ---------------------------------------------
sep = "#"
# [1] == Equal
print(100 == 100)
print(100 == 200)
print(100 == 100.00)

print(sep * 60)

# [2] != Not Equal
print(100 != 100)
print(100 != 200)
print(100 != 100.00)

print(sep * 60)

# [3] >    Greater Than
print(100 > 100)
print(100 > 200)
print(100 > 100.00)
print(100 > 40)

print(sep * 60)

# [4] <    Less Than
print(100 < 100)
print(100 < 200)
print(100 < 100.00)
print(100 < 40)

print(sep * 60)

# [5] >=   Greeter Than or Equal
print(100 >= 100)
print(100 >= 200)
print(100 >= 100.00)
print(100 >= 40)

print(sep * 60)

# [6] >=   Less Than or Equal
print(100 <= 100)
print(100 <= 200)
print(100 <= 100.00)
print(100 <= 40)

print(sep * 60)
