# ---------------------------------------------
# ------------- Boolean Operators -------------
# ---------------------------------------------
# Var One = Self [operator] var two
# Var One [operator]= var two
# [1] =
# [2] +=
# [3] -=
# [4] *=
# [5] /=
# [6] **=
# [7] %=
# [8] //=
# ---------------------------------------------

# [1] =
x = 10
y = 20


# [2] +=
x += y  # x = x + y
print(x)

# [3] -=
x -= y  # x = x - y
print(x)

# [4] *=
x *= y  # x = x * y
print(x)

# [5] /=
x /= y  # x = x / y
print(x)

# [6] **=
x **= y  # x = x ** y
print(x)

# [7] %=
x %= y  # x = x % y
print(x)

# [8] //=
x //= y  # x = x // y
print(x)
